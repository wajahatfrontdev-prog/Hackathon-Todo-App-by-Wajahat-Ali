'use client';

import { useEffect, useState } from 'react';
import { motion } from 'framer-motion';

interface Account {
  email: string;
  name: string;
  avatar: string;
}

export default function GoogleAccountsPage() {
  const [accounts, setAccounts] = useState<Account[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    // Try to get Chrome user info (limited access)
    const getChromeAccounts = async () => {
      try {
        // Check if user is logged into Chrome
        const userAgent = navigator.userAgent;
        const isChrome = userAgent.includes('Chrome');
        
        if (isChrome) {
          // Simulate getting accounts from Chrome (this is a demo)
          // In real implementation, you'd need Google OAuth API
          const mockChromeAccounts = [
            { email: 'your.email@gmail.com', name: 'Your Name', avatar: 'YN' },
            { email: 'work.email@company.com', name: 'Work Account', avatar: 'WA' },
            { email: 'personal@gmail.com', name: 'Personal Account', avatar: 'PA' }
          ];
          setAccounts(mockChromeAccounts);
        } else {
          // Fallback accounts
          setAccounts([
            { email: 'demo@gmail.com', name: 'Demo User', avatar: 'DU' }
          ]);
        }
      } catch (error) {
        console.error('Error getting accounts:', error);
        setAccounts([
          { email: 'fallback@gmail.com', name: 'Fallback User', avatar: 'FU' }
        ]);
      } finally {
        setLoading(false);
      }
    };

    getChromeAccounts();
  }, []);

  function handleAccountSelect(account: Account) {
    setTimeout(() => {
      window.opener?.postMessage({
        type: 'GOOGLE_AUTH_SUCCESS',
        email: account.email,
        name: account.name
      }, window.location.origin);
      window.close();
    }, 500);
  }

  if (loading) {
    return (
      <div className="min-h-screen bg-white flex items-center justify-center">
        <div className="animate-spin h-8 w-8 border-2 border-blue-500 border-t-transparent rounded-full"></div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-white flex items-center justify-center p-4">
      <motion.div 
        initial={{ opacity: 0, scale: 0.95 }}
        animate={{ opacity: 1, scale: 1 }}
        className="w-full max-w-md bg-white rounded-lg border border-gray-200 shadow-lg"
      >
        <div className="p-6 border-b border-gray-200">
          <div className="flex items-center gap-3 mb-4">
            <svg className="w-6 h-6" viewBox="0 0 24 24">
              <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
              <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
              <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
              <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
            </svg>
            <span className="text-xl font-medium text-gray-700">Google</span>
          </div>
          <h1 className="text-2xl font-normal text-gray-900 mb-2">Choose an account</h1>
          <p className="text-sm text-gray-600">to continue to Todo Pro</p>
        </div>

        <div className="p-2">
          {accounts.map((account, index) => (
            <motion.button
              key={account.email}
              initial={{ opacity: 0, y: 10 }}
              animate={{ opacity: 1, y: 0 }}
              transition={{ delay: index * 0.1 }}
              onClick={() => handleAccountSelect(account)}
              className="w-full p-4 hover:bg-gray-50 rounded-lg transition-colors flex items-center gap-4 text-left"
            >
              <div className="w-10 h-10 bg-blue-500 rounded-full flex items-center justify-center text-white font-medium">
                {account.avatar}
              </div>
              <div className="flex-1">
                <div className="font-medium text-gray-900">{account.name}</div>
                <div className="text-sm text-gray-600">{account.email}</div>
              </div>
            </motion.button>
          ))}
        </div>

        <div className="p-4 border-t border-gray-200">
          <button 
            onClick={() => window.close()}
            className="text-sm text-blue-600 hover:underline"
          >
            Use another account
          </button>
        </div>
      </motion.div>
    </div>
  );
}